package bai1;

public class Demo {
	// phương thức mặc định để khởi chạy 1 chương trình Java core
	// phương thức siêu main: public static void main(String[] args){}
	// duy nhất phương thức có cấu trúc như trên mới là siêu main
	// JVM sẽ tìm đến hàm siêu main đầu tiên khi chạy 1 chương trình Java
	public static void main(String[] args) {
		int number = 111;
		System.out.println(number);
		char a = 'a';
		char b = 98;
		System.out.println(a);
		System.out.println(b);
		int c = 'a';
		System.out.println(c);
	}
	// chương trình Java sẽ chạy vs màn hình console
}
